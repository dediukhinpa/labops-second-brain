"""Shared configuration for second_brain services."""
import math
import os
from dataclasses import dataclass, field

from services.shared.tool_gating import parse_tool_set


def _require_pg_password() -> str:
    """Read PG_PASSWORD from env, fail loudly with a clear remediation hint.

    install.sh generates this at install time and writes it to both the .env
    and the systemd EnvironmentFile (/etc/second_brain/secrets.env). If you see this
    error, the most likely cause is that you bypassed install.sh or that the
    secrets file is missing — re-run install.sh, or inspect
    /etc/second_brain/secrets.env to confirm PG_PASSWORD is present.
    """
    pw = os.environ.get("PG_PASSWORD")
    if pw is None:
        raise RuntimeError(
            "PG_PASSWORD is not set. It is generated by scripts/install.sh and "
            "written to /etc/second_brain/secrets.env. Re-run install.sh or export "
            "PG_PASSWORD manually before starting services."
        )
    return pw


def _env_float(name: str, default: str, min_value: float = 0.0) -> float:
    """Parse a float env var with a default and a lower bound.

    Args:
        name: Environment variable name.
        default: Default string value used when the env var is unset.
        min_value: Minimum allowed numeric value (inclusive).

    Returns:
        Parsed float.

    Raises:
        RuntimeError: If the value is not a float or is below ``min_value``.
    """
    raw = os.environ.get(name, default)
    try:
        value = float(raw)
    except (TypeError, ValueError) as exc:
        raise RuntimeError(
            f"{name} must be a float, got: {raw!r}"
        ) from exc
    if not math.isfinite(value):
        raise RuntimeError(
            f"{name} must be finite, got {raw!r}"
        )
    if value < min_value:
        raise RuntimeError(
            f"{name} must be >= {min_value}, got: {value}"
        )
    return value


def _env_float_clamped(
    name: str,
    default: float,
    lo: float,
    hi: float,
) -> float:
    """Parse a float env var, clamp to ``[lo, hi]``, fall back on bad input.

    Designed for hot-path callers where startup must never fail because of an
    operator typo: missing/empty/invalid/NaN/inf all return ``default``, and
    out-of-range values are clamped silently (callers may emit a logger
    warning themselves).

    Args:
        name: Environment variable name.
        default: Fallback value used when the env var is unset / invalid.
        lo: Inclusive lower bound. Values below are clamped up to ``lo``.
        hi: Inclusive upper bound. Values above are clamped down to ``hi``.

    Returns:
        Clamped float in ``[lo, hi]``.
    """
    raw = os.environ.get(name)
    if raw is None or raw == "":
        return default
    try:
        value = float(raw)
    except (TypeError, ValueError):
        return default
    if math.isnan(value) or math.isinf(value):
        return default
    if value < lo:
        return lo
    if value > hi:
        return hi
    return value


def _env_bool(name: str, default: str) -> bool:
    """Parse a boolean env var with a default.

    Accepts ``1``/``0``, ``true``/``false``, ``yes``/``no`` (any case).
    Raises :class:`RuntimeError` on any other value so misconfigurations
    fail loudly at startup instead of silently disabling features.

    Args:
        name: Environment variable name.
        default: Default string value used when the env var is unset.

    Returns:
        Parsed bool.

    Raises:
        RuntimeError: If the value cannot be parsed as a boolean.
    """
    raw = os.environ.get(name, default)
    if raw is None:
        raise RuntimeError(f"{name} default must be a string, got None")
    norm = raw.strip().lower()
    if norm in {"1", "true", "yes", "on"}:
        return True
    if norm in {"0", "false", "no", "off"}:
        return False
    raise RuntimeError(
        f"{name} must be a boolean (1/0/true/false/yes/no), got: {raw!r}"
    )


def _env_int(
    name: str,
    default: str,
    min_value: int = 0,
    max_value: int | None = None,
) -> int:
    """Parse an int env var with a default and inclusive bounds.

    Args:
        name: Environment variable name.
        default: Default string value used when the env var is unset.
        min_value: Minimum allowed integer (inclusive).
        max_value: Optional maximum allowed integer (inclusive).

    Returns:
        Parsed int.

    Raises:
        RuntimeError: If the value is not an int or is out of range.
    """
    raw = os.environ.get(name, default)
    try:
        value = int(raw)
    except (TypeError, ValueError) as exc:
        raise RuntimeError(
            f"{name} must be an int, got: {raw!r}"
        ) from exc
    if value < min_value:
        raise RuntimeError(
            f"{name} must be >= {min_value}, got: {value}"
        )
    if max_value is not None and value > max_value:
        raise RuntimeError(
            f"{name} must be <= {max_value}, got: {value}"
        )
    return value


@dataclass(frozen=True)
class Config:
    """Service configuration loaded from environment variables."""

    pg_host: str = field(
        default_factory=lambda: os.environ.get("PG_HOST", "/var/run/postgresql")
    )
    pg_port: int = field(
        default_factory=lambda: int(os.environ.get("PG_PORT", "5432"))
    )
    pg_database: str = field(
        default_factory=lambda: os.environ.get("PG_DATABASE", "second_brain")
    )
    pg_user: str = field(
        default_factory=lambda: os.environ.get("PG_USER", "second_brain")
    )
    pg_password: str = field(
        default_factory=_require_pg_password
    )
    vault_root: str = field(
        default_factory=lambda: os.environ.get(
            "VAULT_ROOT", "/opt/second_brain/vault"
        )
    )
    fastembed_model: str = field(
        default_factory=lambda: os.environ.get(
            "FASTEMBED_MODEL", "intfloat/multilingual-e5-large"
        )
    )
    mcp_host: str = field(
        default_factory=lambda: os.environ.get("MCP_HOST", "0.0.0.0")
    )
    mcp_port: int = field(
        default_factory=lambda: int(os.environ["MCP_PORT"])
    )
    second_brain_tools: str = field(
        default_factory=lambda: parse_tool_set(os.environ.get("SECOND_BRAIN_TOOLS"))
    )
    rrf_weight_bm25: float = field(
        default_factory=lambda: _env_float("SECOND_BRAIN_RRF_WEIGHT_BM25", "0.4")
    )
    rrf_weight_vec: float = field(
        default_factory=lambda: _env_float("SECOND_BRAIN_RRF_WEIGHT_VEC", "0.6")
    )
    diversify_max: int = field(
        default_factory=lambda: _env_int(
            "SECOND_BRAIN_DIVERSIFY_MAX", "0", min_value=0, max_value=100
        )
    )
    rerank_enabled: bool = field(
        default_factory=lambda: _env_bool("SECOND_BRAIN_RERANK_ENABLED", "0")
    )
    rerank_model: str = field(
        default_factory=lambda: os.environ.get(
            "SECOND_BRAIN_RERANK_MODEL", "jinaai/jina-reranker-v2-base-multilingual"
        )
    )
    rerank_candidates: int = field(
        default_factory=lambda: _env_int(
            "SECOND_BRAIN_RERANK_CANDIDATES", "12", min_value=1, max_value=200
        )
    )
    rerank_max_chars: int = field(
        default_factory=lambda: _env_int(
            # cross-encoder activation memory scales with input length; cap each
            # candidate's text so peak RSS stays under the service memory limit.
            "SECOND_BRAIN_RERANK_MAX_CHARS", "512", min_value=64, max_value=8192
        )
    )
    rerank_onnx_file: str = field(
        default_factory=lambda: os.environ.get(
            # int8-quantized ONNX of rerank_model: ~4x smaller, RU quality intact,
            # fits alongside e5-large under MemoryMax. "onnx/model.onnx" = full model.
            "SECOND_BRAIN_RERANK_ONNX_FILE", "onnx/model_int8.onnx"
        )
    )
    supersede_auto_threshold: float = field(
        default_factory=lambda: _env_float(
            "SECOND_BRAIN_SUPERSEDE_AUTO", "0.85", min_value=0.0
        )
    )
    supersede_hint_threshold: float = field(
        default_factory=lambda: _env_float(
            "SECOND_BRAIN_SUPERSEDE_HINT", "0.70", min_value=0.0
        )
    )
    hmac_timestamp_tolerance_seconds: int = field(
        default_factory=lambda: _env_int(
            "HMAC_TIMESTAMP_TOLERANCE_SECONDS",
            "300",
            min_value=1,
            max_value=86400,
        )
    )
    hmac_auth_enabled: bool = field(
        default_factory=lambda: _env_bool("SECOND_BRAIN_HMAC_AUTH_ENABLED", "1")
    )

    def __post_init__(self) -> None:
        """Cross-field validation.

        Enforces ``supersede_hint_threshold <= supersede_auto_threshold`` so
        the hint band ``[hint, auto)`` is non-empty and the auto band
        ``[auto, 1.0]`` does not overlap into hint territory.

        ``SECOND_BRAIN_SUPERSEDE_AUTO=0.0`` is the documented disable sentinel
        (.env.example: "Set to 0 to disable auto-supersession entirely").
        In that mode the hint > auto cross-validation is intentionally
        skipped because there is no auto band to overlap into -- the hint
        band runs from ``hint_threshold`` to 1.0 and the auto branch is
        permanently disabled inside ``tools.py`` (see line 419 area). The
        upper-bound check on hint still applies so misconfigurations like
        ``HINT=1.5`` continue to fail loudly.
        """
        # Skip cross-field validation when auto is disabled via the sentinel.
        if self.supersede_auto_threshold != 0.0:
            if self.supersede_hint_threshold > self.supersede_auto_threshold:
                raise RuntimeError(
                    "SECOND_BRAIN_SUPERSEDE_HINT "
                    f"({self.supersede_hint_threshold}) must be <= "
                    f"SECOND_BRAIN_SUPERSEDE_AUTO ({self.supersede_auto_threshold})"
                )
            if self.supersede_auto_threshold > 1.0:
                raise RuntimeError(
                    f"SECOND_BRAIN_SUPERSEDE_AUTO ({self.supersede_auto_threshold}) "
                    "must be <= 1.0 (Jaccard cannot exceed 1.0)"
                )
        if self.supersede_hint_threshold > 1.0:
            raise RuntimeError(
                f"SECOND_BRAIN_SUPERSEDE_HINT ({self.supersede_hint_threshold}) "
                "must be <= 1.0 (Jaccard cannot exceed 1.0)"
            )

    def get_pg_dsn(self) -> dict[str, str | int]:
        """Return asyncpg connection kwargs.

        Uses unix socket when pg_host starts with '/', TCP otherwise.
        """
        base: dict[str, str | int] = {
            "database": self.pg_database,
            "user": self.pg_user,
            "password": self.pg_password,
            "host": self.pg_host,
        }
        # Only include explicit port for TCP. Unix-socket hosts (paths starting
        # with '/') ignore port but asyncpg is happy either way.
        if not self.pg_host.startswith("/"):
            base["port"] = self.pg_port
        return base
